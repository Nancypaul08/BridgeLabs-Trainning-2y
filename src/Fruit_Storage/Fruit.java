package Fruit_Storage;

class Fruit {
    public void show() {
        System.out.println("I am a fruit");
    }
}

class Apple extends Fruit {
    public void show() {
        System.out.println("I am an Apple");
    }
}

class Mango extends Fruit {
    public void show() {
        System.out.println("I am a Mango");
    }
}
